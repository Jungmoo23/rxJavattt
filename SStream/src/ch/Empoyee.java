package ch;

public class Empoyee {
	private String name;
	private int salary;
	
	public Empoyee(String name, int salary) {
		this.name = name;
		this.salary = salary;
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSalary() {
		return salary;
	}

	public void setSalary(int salary) {
		this.salary = salary;
	}
	
	

}
